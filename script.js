//function to check for vowel
function isVowel(str){
    const vowels = ['a','e','i','o','u','A','E','I','O','U'];
    let text='';
    if(vowels.includes(str[0])){
       text = `an ${str} pic`;
   } else{
       text = `a ${str} pic`
   }
   return text;
    
}
// function to get the speech/voice
function speakNow(textToSpeak){
        const speech = new SpeechSynthesisUtterance();
        speech.text = textToSpeak;
        window.speechSynthesis.speak(speech);
}
//function to add transition and play sound
function addEffects(){
         const ourText = this.parentElement.previousElementSibling.textContent+' for '+ this.children[0].textContent;
         this.classList.add('current');
         speakNow(ourText);
    }
//function to stop the transition
function removeTransition(e){
        if(e.propertyName === 'transform'){
            this.classList.remove('current');
        }
    
}
//function to play with keyword
function playNow(e){
const containers = document.querySelectorAll('.letter__container');
[...containers].forEach(container=>{
    const title = container.children[0];
    const contents = container.children[1];
    if(e.keyCode === title.textContent.charCodeAt()){
        container.scrollIntoView();
       speakNow(title.textContent);
    }
    
})
}

// get the container that will hold our generated HTML
const container = document.getElementById('container');

// code to generate the HTML
const ourData = alphabets.map(item=>{
    //get the names 
    const animal = item.details.animal.name;
    const bird = item.details.bird.name;
    const flower = item.details.flower.name;
    const fruit = item.details.fruit.name;
    //check if name starts with a vowel
    let animalAlt = isVowel(animal);
    let birdAlt = isVowel(bird);
    let flowerAlt = isVowel(flower);
    let fruitAlt = isVowel(fruit);
    
return `<div class="letter__container">
            <h2 class="main__heading">${item.title}</h2>
            <div class="content__container">
            <div class="content animal">
                <h3 class="content__title">${animal}</h3>
                <div class="image__container">
                    <img src="${item.details.animal.src}" alt="${animalAlt}">
                </div>
            </div>
            <div class="content bird">
                <h3 class="content__title">${bird}</h3>
                <div class="image__container">
                    <img src="${item.details.bird.src}" alt="${birdAlt}">
                </div>
            </div>
            <div class="content flower">
                <h3 class="content__title">${flower}</h3>
                <div class="image__container">
                    <img src="${item.details.flower.src}" alt="${flowerAlt}">
                </div>
        
            </div>
            <div class="content fruits">
                <h3 class="content__title">${fruit}</h3>
                <div class="image__container">
                    <img src="${item.details.fruit.src}" alt="${fruitAlt}">
                </div>
            </div>
            </div>
        </div>`
})


// render the html on the page
container.innerHTML += ourData.join('');




//listening for the events
const contents = document.querySelectorAll('.content');
[...contents].forEach(content=>{
    content.addEventListener('click',addEffects)
     content.addEventListener('transitionend',removeTransition)
    });

window.addEventListener('keydown',playNow);

